# Find out the type
x = 1
y = 2.8
z = 1j
a = 12E4
b = -87.7e100
print(type(x))  # Int
print(type(y))  # Float
print(type(z))  # Complex
print(type(a))  # Float
print(type(b))  # Float

# Casting into Into Integer or String
casting_into_numbers = int("3")
casting_into_string = str(3)
print(type(casting_into_numbers))
print(type(casting_into_string))
